const Catalog = require('../../models/mongoModels/Catalog.js');

const getCatalogs = async function(req, res, next){
  try {
    const catalogs = await Catalog.aggregate([
      { $match: { userId: req.tokenData.userId } },
      {
        $project: {
          _id: 1,
          catalogName: 1,
          chats: 1,
        },
      },
    ]);
    res.send(catalogs);
  } catch (err) {
    next(err);
  }
};

module.exports=getCatalogs;
